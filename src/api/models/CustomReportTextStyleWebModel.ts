/**
 * Through this enum we can apply styles to a text control that supports it
 * These styles roughly follow the HTML section heading elements specification
 * See: https://developer.mozilla.org/en-US/docs/Web/HTML/Element/Heading_Elements
 */
export enum CustomReportTextStyleWebModel {
  Header1 = 'Header1',
  Header2 = 'Header2',
  Header3 = 'Header3',
  Header4 = 'Header4',
  Header5 = 'Header5',
  Normal = 'Normal',
}
